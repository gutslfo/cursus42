#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;

	if (n == 0)
		return (0);
	i = 0;
	while (i < n)
	{
		while ((unsigned char *)s1[i] == (unsigned char *)s2[i])
			i++;
		return (s1[i] - s2[i]);
	}
}