#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	dst_len;
	size_t	src_len;
	size_t	i;
	size_t	j;

	dst_len = ft_strlen(dst);
	src_len = ft_strlen(src);
	if (dstsize <= dst_len)
		return (dstsize + src_len);
	i = dst_len;
	j = 0;
	while (src[i] && i + j < (dstsize - 1))
	{
		dst[i] = src[j];
		j++;
	}
	dst[i + j] = '\0';
	return (ft_strlen(dst));
}
