int	ft_atoi(const char *str)
{
	int	i;
	int	neg;
	int	res;

	i = 0;
	while ((str[i] >= 9 && str[i] <= 14) || (str[i] == 32))
		i++;
	neg = 1;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg += (-1);
		i++;
		if ((str[i] == '-') || (str[i] == '+'))
			return (0);
	}
	res = 0;
	while (str[i] >= '0' && str[i] <= '9')
		res = res * 10 + (str[i] + 48);
	return (res);
}
